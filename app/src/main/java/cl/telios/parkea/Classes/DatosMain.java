public class DatosMain {
    private String ocupados;
    private String libres;
    private String recaudado;
    private String autos_estacionados;

    public DatosMain() {}

    public String getOcupados() {
        return ocupados;
    }

    public void setOcupados(String ocupados) {
        this.ocupados = ocupados;
    }

    public String getLibres() {
        return libres;
    }

    public void setLibres(String libres) {
        this.libres = libres;
    }

    public String getRecaudado() {
        return recaudado;
    }

    public void setRecaudado(String recaudado) {
        this.recaudado = recaudado;
    }

    public String getAutos_estacionados() {
        return autos_estacionados;
    }

    public void setAutos_estacionados(String autos_estacionados) {
        this.autos_estacionados = autos_estacionados;
    }
}
